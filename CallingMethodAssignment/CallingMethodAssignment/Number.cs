using System;
namespace CallingMethodAssignment
{
    public class Math
    {
       public static int Multiply(int Mul)
        {
            int Multiplication = Mul * 12;
            return Multiplication;
        }

        public static int Addition(int add)
        {
            int Sum = add + 12;
            return Sum;
        }

        public static int Division(int Div)
        {
            int divide = Div / 2;
            return divide;
        }
    }
}
